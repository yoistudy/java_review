package ch02.Jaeyun;

public class CastingEx2 {

	public static void main(String[] args) {
		byte b1 = 10;
		int i1 = (int)b1;
		
		System.out.println("i1 = " + i1);
		System.out.println("b1 = " + b1);
		
		int i2 = 300;
		byte b2 = (byte)i2;
		
		System.out.println("i2 = " + i2);
		System.out.println("b2 = " + b2);

	}

}
